void main() async {
  WidgetsFlutterBinding.ensureInitialized();

  // Initialize Firebase
  await Firebase.initializeApp();

  // Initialize notifications
  final notificationService = NotificationService();
  await notificationService.initialize();

  runApp(const LVTodoApp());
}

